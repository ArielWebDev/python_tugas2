#Oprator Aritmatika 
x = 98 
y = 2

print(x + y) #oprator + di gunakan untuk pertambahan antar variable
print(x - y) #oprator - di gunakan untuk pengurangan antar variable
print(x * y) #oprator * di gunakan untuk perkalian antara variable
print(x / y) #oprator / di gunakan untuk pembagian antara variable
print(x % y) #oprator % di gunakan untuk menghitung nilai yang tidak habis di bagi
print(x ** y) #oprator ** di gunakan  untuk perpangkatan antar variable 
print(x // y) #oprator // di gunakan untuk hasil yang di bagi antar variable