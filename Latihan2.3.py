#Oprator Assignment
x = 98 #variable Numeric
x += 1 #oprator untuk menambah dari variable x ya itu 98
print (x)
x -= 5 #oprator untuk mengurangi dari hasil dari pertambahan sebelumnya
print (x)
x *= 3 #oprator untuk perkalian dari hasil pengurangan variable di atas
print (x)
x /= 2 #oprator untuk pembagian dari hasil perkalian di atas
print (x)
x %= 7 #oprator untuk mendapatkan hasil dari yang tidak habis di bagi dari hasil pembagian di atas
print (x)
x //= 3 #oprator untuk medapatkan hasil pembagian yang habis di bagi dari oprasi sebelumnya
print (x)
x **= 3 #oprator untuk memberikan pangkat dari oprasi sebelumnya
print (x)