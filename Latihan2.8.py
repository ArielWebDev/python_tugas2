#Oprator Bitwise
x = 98
y = 12

print(x & y) #oprator and yang menghasilkan angka 1 jika kedua bit bernilai 1.
print(x | y) #oprator or yang menghasilkan angka satu jika jika salah satu bit 1
print(x ^ y) #oprator xor jika salah satu bit 1 maka hasilnya 1 tapi jika keduanya 1 hasilnya 0
print(x << y) #oprator yang mengeser setiap bit ke arah kiri sebanyak yang kita tentukan
print(x >> y) #oprator yang mengeser setiap bit ke arah kiri sebanyak yang kita tentukan