#Oprator Perbandingan
x = 98
y = 5
z = 10

print (x == z) #oprator yang menunjukkan apakah x sama dengan z
print (x == y) #oprator yang menunjukkan apakah x sama dengan y
print (x != z) #oprator yang menunjukkan apakah x tidak sama dengan z
print (x != y) #oprator yang menunjukkan apakah x tidak sama dengan y
print (x < z) #oprator yang menunjukkan apakah x lebih kecil dari z
print (x < y) #oprator yang menunjukkan apakah x lebih kecil dari y
print (x > y) #oprator yang menunjukkan apakah x lebih besar dari y
print (x > z) #oprator yang menunjukkan apakah x lebih besar dari z
print (x <= z) #oprator yang menunjukkan apakah x lebih kecil atau sama dari z
print (x <= y) #oprator yang menunjukkan apakah x lebih kecil atau sama dari y
print (x >= y) #oprator yang menunjukkan apakah x lebih besar atau sama dari y
print (x >= z) #oprator yang menunjukkan apakah x lebih besar atau sama dari z
